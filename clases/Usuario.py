class Persona:
    def __init__(self, nombre, edad, estatura, peso):
        """
        En este metodo sera el que recibira y mandara la informacion 
        de las personas ingresadas. Sus atributos son, nombre,edad
        estatura y por ultimo peso
        """
        self.nombre = nombre
        self.edad = edad
        self.estatura = estatura
        self.peso = peso
